/**
 *  Obtener SVG de icono como string
 */
export const getIconSVG = (iconName, className = '') => {
    const path = ICONS[iconName];
    if (!path) {
        console.warn(`锔 Icono no encontrado: ${iconName}`);
        return '';
    }
    return createIconSVG(path, '0 0 24 24', className);
};

/**
 *  Crear elemento HTML de icono
 */
export const createIconElement = (iconName, className = '') => {
    const svg = getIconSVG(iconName, className);
    const div = document.createElement('div');
    div.innerHTML = svg;
    return div.firstChild;
};

/**
 *  Obtener lista de todos los iconos disponibles
 */
export const getAvailableIcons = () => {
    return Object.keys(ICONS);
};

/**
 *  Buscar iconos por nombre
 */
export const searchIcons = (query) => {
    const lowerQuery = query.toLowerCase();
    return Object.keys(ICONS).filter(name =>
        name.toLowerCase().includes(lowerQuery)
    );
};

/**
 *  Componentes React para iconos
 */
export const Icon = ({ name, className = '', size = 24, color = 'currentColor' }) => {
    const path = ICONS[name];
    if (!path) {
        console.warn(`锔 Icono no encontrado: ${name}`);
        return null;
    }
    
    return React.createElement('svg', {
        xmlns: "http://www.w3.org/2000/svg",
        width: size,
        height: size,
        viewBox: "0 0 24 24",
        fill: "none",
        stroke: color,
        strokeWidth: 2,
        strokeLinecap: "round",
        strokeLinejoin: "round",
        className: className,
        dangerouslySetInnerHTML: { __html: path }
    });
};

export default {
    ICONS,
    createIconSVG,
    getIconSVG,
    createIconElement,
    getAvailableIcons,
    searchIcons,
    Icon
};